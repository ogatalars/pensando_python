def print_twice(x):
    print(x)
    print(x)
    

# Esta função atribui o argumento a um parâmetro chamado bruce. Quando a função é chamada, ela exibe o valor do parâmetro (seja qual for) duas vezes.  


# >>> print_twice('Spam')
# Spam
# Spam
# >>> print_twice(42)
# 42
# 42
# >>> print_twice(math.pi)
# 3.14159265359
# 3.14159265359

# >>> print_twice('Spam '*4)
# Spam Spam Spam Spam
# Spam Spam Spam Spam
# >>> print_twice(math.cos(math.pi))
# -1.0
# -1.0  


elefante = "Eu sou um elefante cor de rosa"
print_twice(elefante)